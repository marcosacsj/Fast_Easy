    public void populaTabela(ResultSet res) {
           DefaultTableModel modelo1 = (DefaultTableModel) tbRelatorio.getModel();
       
        try {
        con=new ConectaBanco();
        
        String sSql = "select NUMEROVOUCHER,EMPRESA,USUARIO,ORIGEM,DESTINO,HSAIDA,HCHEGADA,VALORTOTAL,DATADIA,DATAMES,DATAANO from Voucher where empresa ='"+ClientePesquisa.getText()+"'AND DATAMES='"+DataMes.getSelectedItem()+"'and dataano = '"+Ano1.getSelectedItem()+"';";
        con.conecta();
         if(!con.conecta())
        {
            InOut.MsgDeErro("AppAdm","Erro ao conectar");
            return;            
        }
        con.executaSelect(sSql);
       
        System.out.println(sSql);
        lRes = con.resultset;      
        System.out.println(lRes);
            modelo1.setRowCount(0);
            while (lRes.next()) {
                modelo1.addRow(new Object[]{
                            lRes.getString("NUMEROVOUCHER"),
                            lRes.getString("EMPRESA"),
                            lRes.getString("USUARIO"),
                            lRes.getString("ORIGEM"),
                            lRes.getString("DESTINO"),
                            lRes.getString("HSAIDA"),
                            lRes.getString("HCHEGADA"),
                            lRes.getString("VALORTOTAL"),
                            lRes.getString("DATADIA"),
                            lRes.getString("DATAMES"),
                            lRes.getString("DATAANO"),
                            
                            
                            
                            
                            
                            
                            
                            });

            }
            tbRelatorio.setModel(modelo1);
        } catch (SQLException ex) {

            JOptionPane.showMessageDialog(ClientePesquisa, ex);
        }
        con.desconecta();
    }
    
  
