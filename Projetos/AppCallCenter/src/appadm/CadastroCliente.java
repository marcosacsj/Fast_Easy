private void TabPesqEmpresaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TabPesqEmpresaMouseClicked
// TODO add your handling code here:
    int linha = 0;
    // int coluna=0;
    TabPesqEmpresa.setVisible(true);
    linha = TabPesqEmpresa.getSelectedRow();
    //Empresa1.setText(TabPesqCliente.getValueAt(linha, coluna).toString());

    sCliente = TabPesqEmpresa.getValueAt(linha, 0).toString();
    // TODO add your handling code here:
    con = new ConectaBanco();
    String sSql = new String();
    sSql = "Select * from cadempresa where nomeempresa='" + sCliente + "';";
    con.conecta();
    if (!con.conecta()) {
        InOut.MsgDeErro("AppAdm", "Erro ao conectar");
        return;
    }
    con.executaSelect(sSql);

    lRes = con.resultset;
    try {
        while (lRes.next()) {
            //r =Integer.parseInt(lRes.getString("IdVoucher"));
            // Number.setText(lRes.getString("NumeroVoucher"));
            EmpresaNome.setText(lRes.getString("NOMEEMPRESA"));
            EmpresaCNPJ.setText(lRes.getString("CNPJ"));
            EmpresaRS.setText(lRes.getString("RAZAOSOCIAL"));
            EmpresaIE.setText(lRes.getString("INSESTADUAL"));
            EmpresaEnd.setText(lRes.getString("RUA"));
            EmpresaNum.setText(lRes.getString("NUMERO"));
            EmpresaBairro.setText(lRes.getString("BAIRRO"));
            EmpresaCidade.setText(lRes.getString("CIDADE"));
            EmpresaEstado.setText(lRes.getString("ESTADO"));
            EmpresaCEP.setText(lRes.getString("CEP"));
            ValorKM.setText(lRes.getString("VALORPORKM"));
            lRes.getString("TELEFONEEMPRESA");


        }
    } catch (SQLException ex) {
        Logger.getLogger(ExecutiveService.class.getName()).log(Level.SEVERE, null, ex);
    }

    con.desconecta();

}//GEN-LAST:event_TabPesqEmpresaMouseClicked

