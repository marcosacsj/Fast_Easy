    public void populaTabela1(ResultSet res) {
        DefaultTableModel modelo1 = (DefaultTableModel) TabPesqEmpresa.getModel();
        try {
            modelo1.setRowCount(0);
            while (res.next()) {
                modelo1.addRow(new Object[]{
                            res.getString("NOMEEMPRESA"),
                            res.getString("CNPJ"),
                            res.getString("RUA"),
                            res.getString("NUMERO"),
                            res.getString("CIDADE"),});

            }
            TabPesqEmpresa.setModel(modelo1);
        } catch (SQLException ex) {

            JOptionPane.showMessageDialog(TabPesqEmpresa, ex);
        }
    }

