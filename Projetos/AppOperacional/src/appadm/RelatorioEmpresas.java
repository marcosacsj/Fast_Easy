    private void PopulaTabela() {
        DefaultTableModel modelo1 = (DefaultTableModel) TabEmpresas.getModel();
       
        try {
        con=new ConectaBanco();
        String sSql=new String();
        sSql="select * from cadempresa";
        con.conecta();
        con.executaSelect(sSql);
        System.out.println(sSql);
        lRes = con.resultset;      
        
            modelo1.setRowCount(0);
            while (lRes.next()) {
                modelo1.addRow(new Object[]{
                            lRes.getString("nomeempresa"),
                            lRes.getString("cnpj"),
                            lRes.getString("liberado")
                            });

            }
            TabEmpresas.setModel(modelo1);
        } catch (SQLException ex) {

            JOptionPane.showMessageDialog(TabEmpresas, ex);
        }
        con.desconecta();
        
    }
